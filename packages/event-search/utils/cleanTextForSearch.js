function cleanText(lang, text) {
  if (typeof text !== 'string') {
    return text;
  }

  return text
    .split(' ')
    .filter(
      (term) =>
        !filteredTerms[lang]
        || !filteredTerms[lang].includes(term.toLowerCase()),
    )
    .join(' ');
}

export default function cleanTextForSearch(text) {
  if (!text) {
    return undefined;
  }

  if (typeof text === 'string') {
    return cleanText('fr', text);
  }

  if (Array.isArray(text)) {
    return text.map((textItem) => cleanText('fr', textItem));
  }

  if (typeof text === 'object') {
    return Object.keys(text)
      .map((lang) => cleanText(lang, text[lang]))
      .filter((t) => !!t);
  }

  return text;
}
